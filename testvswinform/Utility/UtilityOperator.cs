using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace testvswinform.Utility
{
    public class UtilityOperator
    {
        //http://www.cnblogs.com/zjbtony/archive/2012/04/13/2445055.html
